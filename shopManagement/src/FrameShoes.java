    private void btnDelCusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelCusActionPerformed
        int selected = tbCus.getSelectedRow();
        if(selected >=0 )
        {
            Customer std = cusList.get(selected);
            int sel = JOptionPane.showConfirmDialog(this, "Your choice cannot remake! Sure to delete?");
            System.out.println("option: " +sel);
            
            if(sel == 0)
            {
                ConnectCustomer.deleteCustomer(std.getId());
                
                showCustomer();
            }
        }
    }//GEN-LAST:event_btnDelCusActionPerformed

