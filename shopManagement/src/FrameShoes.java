    private void btnAddShoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddShoesActionPerformed
        try{
        int shoesid = Integer.parseInt(txtID.getText());
        String brand = cbBrand.getSelectedItem().toString();
        String nameofshoes = txtNameOfShoes.getText();
        Double size = Double.parseDouble(cbSize.getSelectedItem().toString());
        int quantity = Integer.parseInt(txtQuantity.getText());
        Double price = Double.parseDouble(txtPrice.getText());
        Double total = Integer.parseInt(txtQuantity.getText()) * Double.parseDouble(txtPrice.getText());
        
        
        Shoes std = new Shoes(shoesid, quantity, brand, nameofshoes, size, price, total);
        
        ConnectSQL.addShoesData(std);
        }
        catch(Exception e){
            txtID.setText("");
            txtQuantity.setText("");
            cbBrand.setSelectedIndex(0);
            txtNameOfShoes.setText("");
            cbSize.setSelectedIndex(0);
            txtPrice.setText("");
            JOptionPane.showMessageDialog(this, "Your information you input have a problem!! Please Input again");
        }
        finally{
            showShoes();
        }
    }//GEN-LAST:event_btnAddShoesActionPerformed

    private void btnSearchShoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchShoesActionPerformed
        String input = JOptionPane.showInputDialog(this, "Enter Name of Shoes to search");
        
            try
            {
                shoesList = ConnectSQL.searchByName(input);
                tableModel.setRowCount(0);
            
                shoesList.forEach(shoes -> {
                tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, shoes.getBrand(),
                shoes.getNameofshoes(), shoes.getSize(), shoes.getQuantity(), shoes.getPrice(), shoes.getTotal()});
            });
            }catch(Exception e){
                showShoes();
            }
    }//GEN-LAST:event_btnSearchShoesActionPerformed

