    private void  showShoes()
    {
        shoesList = ConnectSQL.getShoesData();
        
        tableModel.setRowCount(0);
        
        shoesList.forEach(shoes -> {
            tableModel.addRow(new Object[] {tableModel.getRowCount() + shoes.getShoesid(), shoes.getBrand(),
                shoes.getNameofshoes(), shoes.getSize(), shoes.getQuantity(), shoes.getPrice(), shoes.getTotal()});
        });
    }
    
    private void showCustomer()
    {
        cusList = ConnectCustomer.getCustomerData();
        
        tableCustomer.setRowCount(0);
        
        cusList.forEach(customer -> {
            tableCustomer.addRow(new Object[] {tableCustomer.getRowCount() + customer.getId(), customer.getName(),
                customer.getAddress(), customer.getPhonenumber(), customer.getShoesid()});
        });
    }
